    private void loadcoustomer(String column, String method, String mobile) {

        try {
            ResultSet resultSet = MySQL.execute("SELECT * FROM `customer` WHERE `mobile` LIKE '" + mobile + "%' ORDER BY `" + column + "` " + method + " ");

            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);

            while (resultSet.next()) {

                Vector<String> vector = new Vector<>();
                vector.add(resultSet.getString("mobile"));
                vector.add(resultSet.getString("first_name"));
                vector.add(resultSet.getString("last_name"));
                vector.add(resultSet.getString("email"));
                vector.add(resultSet.getString("points"));

                model.addRow(vector);

            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    private void search() {

        int sortIndex = jComboBox1.getSelectedIndex();

        if (sortIndex == 0) {
            loadcoustomer("first_name","ASC", jTextField1.getText());

        } else if (sortIndex == 1) {
            loadcoustomer("first_name","DESC", jTextField1.getText());

        } else if (sortIndex == 2) {
            loadcoustomer("points", "ASC",jTextField1.getText());

        } else if (sortIndex == 3) {
            loadcoustomer("points","DESC", jTextField1.getText());
        }

    }

